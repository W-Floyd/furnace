################################################################################
#
# __graph <GRAPH_FORMAT> <GRAPH_SEED> <GRAPH_FILE>
#
# Graph
# Wrapper for graphviz
#
################################################################################

__graph () {

local __prefix='graph'

__choose_function -e -d 'dependency graphing' -p 'sfdp neato' "${__prefix}"

__run_routine "${__prefix}" ${@}

}

################################################################################
#
# __graph_common <GRAPHER> <GRAPH_FORMAT> <GRAPH_SEED> <GRAPH_FILE>
#
# Graph Common
#
################################################################################

__graph_common () {
local __grapher="${1}"

shift

"${__grapher}" -Ln5 "-Gstart=${2}" "-T${1}" "${3}" -O
}

################################################################################
#
# In all cases:
# __routine__graph__<GRAPHER> <GRAPH_FORMAT> <GRAPH_SEED> <GRAPH_FILE>
#
# <GRAPHER> Graph
#
################################################################################

################################################################################
# dot
################################################################################

__routine__graph__dot () {

